package spring.model;

public class AdminCommand {
	private String conhash;
	private int hashlike;
	private int hashcontent;
	public String getConhash() {
		return conhash;
	}
	public void setConhash(String conhash) {
		this.conhash = conhash;
	}
	public int getHashlike() {
		return hashlike;
	}
	public void setHashlike(int hashlike) {
		this.hashlike = hashlike;
	}
	public int getHashcontent() {
		return hashcontent;
	}
	public void setHashcontent(int hashcontent) {
		this.hashcontent = hashcontent;
	}
	
	

}
